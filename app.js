const numArrays = [
	[100, 5, 23],
	[15, 21, 72, 9],
	[45, 66],
	[7, 81, 90]];

let total = 0;

numArrays.forEach(innerArray => {

    innerArray.forEach(number => 
        { total += number;  });
});

console.log('Exercise 15 result:\n', total); 
